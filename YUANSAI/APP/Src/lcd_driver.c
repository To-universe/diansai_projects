{
}

/* ======================== 字符显示 ======================== */

void LCD_SetTextColor(uint16_t color)
{
    text_color = color;
}

void LCD_SetBackColor(uint16_t color)
{
    back_color = color;
}

void LCD_ShowChar(uint16_t x, uint16_t y, char ch)
{
    uint8_t index = (uint8_t)ch - 0x20;  /* 字库从空格开始 */

    if (index > 0x7E - 0x20) index = 0;  /* 不支持的字符用空格替代 */

    const uint8_t *ptr = &font8x16_table[index][0];

    LCD_SetWindow(x, y, 8, 16);

    for (uint8_t row = 0; row < 16; row++) {
        uint8_t line = ptr[row];
        for (uint8_t col = 0; col < 8; col++) {
            if (line & (0x80 >> col))
                LCD_DATA = text_color;
            else
                LCD_DATA = back_color;
        }
    }
}

void LCD_ShowString(uint16_t x, uint16_t y, const char *str)
{
    while (*str) {
        if (*str == '\n') {
            y += 16;
            x = 0;  /* 回到行首（可根据需要调整） */
        } else {
            LCD_ShowChar(x, y, *str);
            x += 8;
            if (x + 8 > LCD_WIDTH) {
                x = 0;
                y += 16;
            }
        }
        str++;
    }
}

