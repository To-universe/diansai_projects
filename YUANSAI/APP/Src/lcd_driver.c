/* ======================== Window ======================== */

void LCD_SetWindow(uint16_t x, uint16_t y, uint16_t w, uint16_t h)
{
    uint16_t x2 = x + w - 1, y2 = y + h - 1;
    LCD_WriteCmd(0x2A);
    LCD_DATA = x >> 8; LCD_DATA = x & 0xFF;
    LCD_DATA = x2 >> 8; LCD_DATA = x2 & 0xFF;
    LCD_WriteCmd(0x2B);
    LCD_DATA = y >> 8; LCD_DATA = y & 0xFF;
    LCD_DATA = y2 >> 8; LCD_DATA = y2 & 0xFF;
    LCD_WriteCmd(0x2C);
}

/* ======================== Drawing ======================== */

void LCD_DrawPoint(uint16_t x, uint16_t y, uint16_t color)
{
    if (x >= LCD_WIDTH || y >= LCD_HEIGHT) return;
    LCD_SetWindow(x, y, 1, 1);
    LCD_DATA = color;
}

void LCD_FillRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    uint32_t total = (uint32_t)w * h;
    if (x >= LCD_WIDTH || y >= LCD_HEIGHT) return;
    if (x + w > LCD_WIDTH)  w = LCD_WIDTH - x;
    if (y + h > LCD_HEIGHT) h = LCD_HEIGHT - y;
    LCD_SetWindow(x, y, w, h);
    for (uint32_t i = 0; i < total; i++) LCD_DATA = color;
}

void LCD_Clear(uint16_t color) { LCD_FillRect(0, 0, LCD_WIDTH, LCD_HEIGHT, color); }

void LCD_DrawImage(uint16_t x, uint16_t y, uint16_t w, uint16_t h, const uint8_t *pixels)
{
    uint32_t total = (uint32_t)w * h;
    if (x >= LCD_WIDTH || y >= LCD_HEIGHT) return;
    if (x + w > LCD_WIDTH)  w = LCD_WIDTH - x;
    if (y + h > LCD_HEIGHT) h = LCD_HEIGHT - y;
    LCD_SetWindow(x, y, w, h);
    const uint16_t *px = (const uint16_t *)pixels;
    for (uint32_t i = 0; i < total; i++) LCD_DATA = px[i];
}

/* ======================== Text ======================== */

void LCD_SetTextColor(uint16_t c) { text_color = c; }
void LCD_SetBackColor(uint16_t c) { back_color = c; }

void LCD_ShowChar(uint16_t x, uint16_t y, char ch)
{
    uint8_t idx = (uint8_t)ch - 0x20;
    if (idx > 0x7E - 0x20) idx = 0;
    LCD_SetWindow(x, y, 8, 16);
    const uint8_t *p = font8x16_table[idx];
    for (uint8_t r = 0; r < 16; r++) {
        uint8_t line = p[r];
        for (uint8_t c = 0; c < 8; c++)
            LCD_DATA = (line & (0x80 >> c)) ? text_color : back_color;
    }
}

void LCD_ShowString(uint16_t x, uint16_t y, const char *str)
{
    while (*str) {
        if (*str == '\n') { y += 16; x = 0; }
        else { LCD_ShowChar(x, y, *str); x += 8; if (x + 8 > LCD_WIDTH) { x = 0; y += 16; } }
        str++;
    }
}

