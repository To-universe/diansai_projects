/* ======================== 显示窗口设置 ======================== */

void LCD_SetWindow(uint16_t x, uint16_t y, uint16_t width, uint16_t height)
{
    uint16_t x1 = x;
    uint16_t y1 = y;
    uint16_t x2 = x + width - 1;
    uint16_t y2 = y + height - 1;

    LCD_WriteCmd(0x2A);  /* 列地址设置 */
    LCD_DATA = x1 >> 8;
    LCD_DATA = x1 & 0xFF;
    LCD_DATA = x2 >> 8;
    LCD_DATA = x2 & 0xFF;

    LCD_WriteCmd(0x2B);  /* 页地址设置 */
    LCD_DATA = y1 >> 8;
    LCD_DATA = y1 & 0xFF;
    LCD_DATA = y2 >> 8;
    LCD_DATA = y2 & 0xFF;

    LCD_WriteCmd(0x2C);  /* 写 GRAM */
}

/* ======================== 基本绘图 ======================== */

void LCD_DrawPoint(uint16_t x, uint16_t y, uint16_t color)
{
    if (x >= LCD_WIDTH || y >= LCD_HEIGHT) return;

    LCD_SetWindow(x, y, 1, 1);
    LCD_DATA = color;
}

void LCD_FillRect(uint16_t x, uint16_t y, uint16_t w, uint16_t h, uint16_t color)
{
    uint32_t total = (uint32_t)w * h;

    if (x >= LCD_WIDTH || y >= LCD_HEIGHT) return;
    if (x + w > LCD_WIDTH)  w = LCD_WIDTH - x;
    if (y + h > LCD_HEIGHT) h = LCD_HEIGHT - y;

    LCD_SetWindow(x, y, w, h);

    /* 16位批量写入 — 直接循环 */
    for (uint32_t i = 0; i < total; i++) {
        LCD_DATA = color;
    }
}

void LCD_Clear(uint16_t color)
{
    LCD_FillRect(0, 0, LCD_WIDTH, LCD_HEIGHT, color);
}

/* ======================== 图片显示 ======================== */

/*
 * 将像素数据绘制到屏幕上（如 JPEG 解码后的灰度图）
 * pixels: RGB565 格式, 每像素 2 字节 (低字节在前)
 */
void LCD_DrawImage(uint16_t x, uint16_t y, uint16_t w, uint16_t h, const uint8_t *pixels)
{
    uint32_t total = (uint32_t)w * h;

    if (x >= LCD_WIDTH || y >= LCD_HEIGHT) return;
    if (x + w > LCD_WIDTH)  w = LCD_WIDTH - x;
    if (y + h > LCD_HEIGHT) h = LCD_HEIGHT - y;

    LCD_SetWindow(x, y, w, h);

    const uint16_t *px = (const uint16_t *)pixels;
    for (uint32_t i = 0; i < total; i++) {
        LCD_DATA = px[i];
    }
}

static uint16_t LCD_GrayToRgb565(uint8_t gray)
{
    return (uint16_t)(((uint16_t)(gray & 0xF8U) << 8)
                    | ((uint16_t)(gray & 0xFCU) << 3)
                    | ((uint16_t)gray >> 3));
}

void LCD_PrepareFrame(const uint8_t *gray_pixels)
{
    const uint16_t src_w = 48U;
    const uint16_t src_h = 36U;
    const uint16_t scale = 3U;
    const uint16_t dst_x = 48U;
    const uint16_t dst_y = 10U;

    if (gray_pixels == 0) {
        return;
    }

    LCD_SetWindow(dst_x, dst_y, src_w * scale, src_h * scale);

    for (uint16_t y = 0; y < src_h; y++) {
        for (uint16_t sy = 0; sy < scale; sy++) {
            for (uint16_t x = 0; x < src_w; x++) {
                uint16_t color = LCD_GrayToRgb565(gray_pixels[y * src_w + x]);

                for (uint16_t sx = 0; sx < scale; sx++) {
                    LCD_DATA = color;
                }
            }
        }
    }
}

